/**
 * Checks if a number is even.
 * @param value The number to check.
 * @returns True if the number is even, false otherwise.
 * @example
 * ```ts
 * const result = isEvenNumber(4);
 * console.log(result);
 * // Output: true
 * ```
 */
export const isEvenNumber = (value: number): boolean => value % 2 === 0;
